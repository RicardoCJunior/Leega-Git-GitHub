package Exercicios;

public class Exer03 {
    public static void main(String[] args) {

//Exercício 3: Laços (for/while)
//Crie um programa que imprima os números de 1 a 10 usando um loop.

        for (int i = 1; i <= 10; i++) {

            System.out.println(i);

        }

        int x = 1;
        while (x <= 10) {
            System.out.println(x);
            x++;
        }

    }

}
